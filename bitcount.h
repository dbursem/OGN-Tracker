inline uint8_t u8Count1s(uint8_t Byte)
{
    return ByteCount1s[Byte];
}
inline uint8_t u32Count1s(uint32_t uWord)
{
  uint8_t count = 0;
  uint8_t i;
  for(i=0;i<4;i++)
  {
    count += u8Count1s( uWord & 0x000000FF);
    uWord = uWord >> 8;
  }
  return count;
}


